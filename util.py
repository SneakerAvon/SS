def load_dataset(dataset_dir, batch_size, valid_batch_size= None, test_batch_size=None):
    data = {}
    #循环遍历['train', 'val', 'test']，分别从数据集目录中加载名为category的数据集，将其分别存储在字典data中的x_category和y_category中
    #x_category代表数据集中的输入，y_category代表数据集中的标签。
    for category in ['train', 'val', 'test']:
        cat_data = np.load(os.path.join(dataset_dir, category + '.npz'))
        data['x_' + category] = cat_data['x']
        data['y_' + category] = cat_data['y']
    #使用StandardScaler函数对数据进行标准化处理，计算均值和标准差，并将其存储在名为scaler的变量中。这里默认使用训练集的均值和标准差。
    scaler = StandardScaler(mean=data['x_train'][..., 0].mean(), std=data['x_train'][..., 0].std())
    # Data format
    #将所有数据集的输入部分应用标准化处理
    for category in ['train', 'val', 'test']:
        data['x_' + category][..., 0] = scaler.transform(data['x_' + category][..., 0])
    #使用DataLoader函数将所有数据集分别装载成可迭代的数据加载器，将其存储在字典data中的train_loader，val_loader和test_loader中。
    data['train_loader'] = DataLoader(data['x_train'], data['y_train'], batch_size)
    data['val_loader'] = DataLoader(data['x_val'], data['y_val'], valid_batch_size)
    data['test_loader'] = DataLoader(data['x_test'], data['y_test'], test_batch_size)
    #将标准化处理的对象存储在字典data中的scaler中
    data['scaler'] = scaler
    return data

def masked_mse(preds, labels, null_val=np.nan):
    if np.isnan(null_val):
        mask = ~torch.isnan(labels)
    else:
        mask = (labels!=null_val)
    mask = mask.float()
    mask /= torch.mean((mask))
    mask = torch.where(torch.isnan(mask), torch.zeros_like(mask), mask)
    loss = (preds-labels)**2
    loss = loss * mask
    loss = torch.where(torch.isnan(loss), torch.zeros_like(loss), loss)
    return torch.mean(loss)

